package brains;

/**
 *  A function that returns something given {@code x}.<P>
 *  Implementors of {@code output} are required to be thread safe.
 */
public interface IFunction
{
	/**
	 *  A function that returns something given {@code x}.<P>
	 *  Implementors of {@code output} are required to be thread safe.
	 */
	public float output(float x);
}
